function color_check(button) {
    if (color == "black") {
        button.style.backgroundColor = "black";
    }
    else if (color == ""){
        button.style.backgroundColor = "";
    }
    else if (color == "red"){
        button.style.backgroundColor = "red";
    }
    else if (color == "blue"){
        button.style.backgroundColor = "blue";
    }
    else if (color == "green"){
        button.style.backgroundColor = "green"
    }
}